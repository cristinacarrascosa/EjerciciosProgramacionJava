public class Ej1B {

    
    public static void main(String[] args) {
        
        int x = 10;
        System.out.println("Valor de x: "+x);
        //Salir cuando llega a 1
        
        do{        
            System.out.println(x);
            x--;  
        } while(x>=1);
    }
    
}
